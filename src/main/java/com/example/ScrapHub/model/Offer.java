package com.example.ScrapHub.model;

public class Offer {
    private int reqQuantity;
    private int reqPrice;
    private String reqPhoneNumber;
    public int getReqQuantity() {
        return reqQuantity;
    }
    public void setReqQuantity(int reqQuantity) {
        this.reqQuantity = reqQuantity;
    }
    public int getReqPrice() {
        return reqPrice;
    }
    public void setReqPrice(int reqPrice) {
        this.reqPrice = reqPrice;
    }
    public String getReqPhoneNumber() {
        return reqPhoneNumber;
    }
    public void setReqPhoneNumber(String reqPhoneNumber) {
        this.reqPhoneNumber = reqPhoneNumber;
    }
    
}
